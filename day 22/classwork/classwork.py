# 1) Write a program that calculates and prints the sum of numbers from 1 to 10 using a for loop
# შექმენით პროგრამა რომელიც გამოთვლის და დაპრინტავს რიცხვების ჯამს 1 დან 10 ჩათვლით for loop-ების დახმარებით
sum=0
for i in range(1,11):
    sum +=i
print("1 დან 10ის ჩათვლით რიცხვების ჯამი",sum)


